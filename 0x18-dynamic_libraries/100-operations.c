#include <stdio.h>

/**
 * add - Adds two integers.
 * @a: first int.
 * @b: Second int.
 * Return: Final answer.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtract two integers.
 * @a: First int.
 * @b: Second int.
 * Return: Final answer.
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiply two integers.
 * @a: First int.
 * @b: Second int.
 * Return: Answer
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divide two integers.
 * @a: First int.
 * @b: Second int.
 * Return: Final answer.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Divides  two integers.
 * @a: First int.
 * @b: Second int.
 * Return: Final answer.
 */
int mod(int a, int b)
{
	return (a % b);
}
